#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    cout << cbrt(8) << endl;   // cube root
    cout << sqrt(4) << endl;   // square root
    cout << max(8, 5) << endl; // max of two num
    cout << min(8, 5) << endl; // min of two num
    cout << pow(8, 5) << endl; // power num
}